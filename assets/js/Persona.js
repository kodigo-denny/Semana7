class Persona{
    dui// ID
    nombre
    apellido
    direccion
    telefono
    correo

    constructor(dui, nombre, apellido, direccion, telefono, correo){
        this.dui = dui;
        this.nombre = nombre,
        this.apellido = apellido;
        this.direccion = direccion;
        this.telefono = telefono;
        this.correo = correo;
    }
}